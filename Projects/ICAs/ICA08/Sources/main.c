void main(void)
{
  // main entry point - these two lines must appear first
  _DISABLE_COP();
  EnableInterrupts;

  /////////////////////////////////////////////////////////////////////////////
  // one-time initializations
  /////////////////////////////////////////////////////////////////////////////
  PLL_To20MHz();
  SWL_Init();
  (void)PIT_Init(2000000ul, 1000000);
  /////////////////////////////////////////////////////////////////////////////
  // main program loop
  /////////////////////////////////////////////////////////////////////////////
  for (;;)
  {
    //  struct pitvalues = GenPITValues(20000000ul, 100000);
    // poll for pit flag, on channel 0
    if (PITTF_PTF0)
    {

      // acknowledge the flag
      PITTF = PITTF_PTF0_MASK;//clears the flag

      // toogle the red led
      SWL_TOG(SWL_RED);
    }
  }
}

